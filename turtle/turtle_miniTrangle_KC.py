import turtle


window = turtle.Screen()
t = turtle.Turtle()
t.speed(0)
t.shape("turtle")
t.color("blue")
t.setposition(0,-200)
t.setposition(0,-100)
t.setposition(120,0)
t.setposition(0,-100)
t.setposition(120,-200)
t.penup()
t.setposition(150,0)
t.pendown()
t.setposition(250,0)
t.setposition(150,0)
t.setposition(150,-200)
t.setposition(250,-200)
window.exitonclick()


	    



